const HeaderLeft = () => {
  const [anchorEl, setAnchorEl] = useState<null | HTMLElement>(null);
  const open = Boolean(anchorEl);
  const navigate = useNavigate();

  const handleClick = (event: React.MouseEvent<HTMLElement>) => {
    setAnchorEl(event.currentTarget);
  };
  const handleClose = () => {
    setAnchorEl(null);
  };

  function handleLogout() {
    localStorage.removeItem("token");
    return navigate("/admin/login", { replace: true });
  }
  const { mode, changeMode } = useContext(ThemeContext);
  const { user } = useContext(AuthContext);
  return (
    <Box
      sx={{
        display: "flex",
        alignItems: "center",
      }}
    >
      <FormGroup>
        <FormControlLabel
          control={<MaterialUISwitch checked={mode} onChange={() => changeMode()} />}
          label=""
        />
      </FormGroup>

      <IconButton color="secondary" aria-label="add an alarm">
        <NotificationsNoneIcon fontSize="large" />
      </IconButton>

      <Box
        sx={{ ml: 2, display: "flex", alignItems: "center", gap: 1, cursor: "pointer" }}
        onClick={handleClick}
      >
        <Typography variant="h5">{user?.firstName || user?.username}</Typography>
        <Tooltip title="Tài khoản">
          <IconButton
            size="small"
            aria-controls={open ? "account-menu" : undefined}
            aria-haspopup="true"
            aria-expanded={open ? "true" : undefined}
          >
            <Avatar sx={{ width: 32, height: 32 }} src={user?.avatar || ""}></Avatar>
          </IconButton>
        </Tooltip>
      </Box>

      <Menu
        anchorEl={anchorEl}
        id="account-menu"
        open={open}
        onClose={handleClose}
        onClick={handleClose}
        slotProps={{
          paper: {
            elevation: 0,
            sx: {
              overflow: "visible",
              filter: "drop-shadow(0px 2px 8px rgba(0,0,0,0.32))",
              mt: 1.5,
              "& .MuiAvatar-root": {
                width: 32,
                height: 32,
                ml: -0.5,
                mr: 1,
              },
              "&::before": {
                content: '""',
                display: "block",
                position: "absolute",
                top: 0,
                right: 14,
                width: 10,
                height: 10,
                bgcolor: "background.paper",
                transform: "translateY(-50%) rotate(45deg)",
                zIndex: 0,
              },
            },
          },
        }}
        transformOrigin={{ horizontal: "right", vertical: "top" }}
        anchorOrigin={{ horizontal: "right", vertical: "bottom" }}
      >
        <MenuItem onClick={handleClose}>
          <Avatar src={user?.avatar || ""} /> Hồ Sơ
        </MenuItem>
        <MenuItem onClick={handleClose}>
          <ListItemIcon>
            <Settings fontSize="medium" />
          </ListItemIcon>
          Cài Đặt
        </MenuItem>
        <Divider sx={{ bgcolor: "text.primary" }} />
        <MenuItem onClick={handleLogout}>
          <ListItemIcon>
            <Logout fontSize="medium" />
          </ListItemIcon>
          Đăng Xuất
        </MenuItem>
      </Menu>
    </Box>
  );
};

export default HeaderLeft;
